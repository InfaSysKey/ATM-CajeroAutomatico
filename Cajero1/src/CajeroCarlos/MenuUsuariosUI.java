    /**
     * Muestra los usuarios que hay.
     * Con el boolean bloqueamos las celdas que queremos.
     */
    public void muestraUsuarios() {
        List<Usuario> listaUsuarios = ServicioBBDD.getUsuarios();
        DefaultTableModel modelo = new DefaultTableModel(0,3){
        @Override
        public boolean isCellEditable (int row, int column){
            return column == 1 || column == 2;
        }
        };
        for (Usuario usuario : listaUsuarios) {
            int idUsuario = usuario.getIdUsuario();
            String nombre = usuario.getNombre();
            String apellido = usuario.getApellido();
            Object[] datos = {idUsuario,nombre,apellido};
            //AÃ±adir a la tabla
            modelo.addRow(datos);
        }
        modelo.setColumnIdentifiers(new String[]{"ID_Usuario", "Nombre", "Apellido"});
        this.resultados.setModel(modelo);
        modelo.fireTableDataChanged();

    }

