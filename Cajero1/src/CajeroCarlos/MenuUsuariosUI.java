    private void butModifiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_butModifiActionPerformed
        try {
            int fila = resultados.getSelectedRow();
            if (resultados.isRowSelected(fila)) {
                int idUsuario =  (int) resultados.getValueAt(fila, 0);
                String nombre  = (String) resultados.getValueAt(fila, 1);
                String apellido =   (String) resultados.getValueAt(fila, 2);
                System.out.println(idUsuario);
                System.out.println(nombre);
                System.out.println(apellido);
                ServicioBBDD.updateUsuario(idUsuario,nombre,apellido);
            }
        } catch (ClassCastException ev) {
            JOptionPane.showMessageDialog(this, "Ningun dato actualizado", "Advetencia", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_butModifiActionPerformed

