    /**
     * Borra la cuenta seleccionada
     */
     public void borrarUsuariosSeleccionada() {
        int indice = this.resultados.getSelectedRow();

        int idUsuario = (int) this.resultados.getValueAt(indice, 0);
        ServicioBBDD.borrarUsuario(idUsuario);
        this.muestraUsuarios();
    }
