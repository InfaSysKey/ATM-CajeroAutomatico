    /**
     * Muestra las cuentas que hay.
     * Con el boolean bloqueamos las celdas que queremos.
     */
    public void muestraCuentas() {
        List<Cuenta> listaCuentas = ServicioBBDD.getCuentas();
        DefaultTableModel modelo = new DefaultTableModel(0, 3) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return column == 2;
            }
        };
        for (Cuenta cuenta : listaCuentas) {
            int idCuenta = cuenta.getIdCuenta();
            int idUsuario = cuenta.getIdUsuario();
            int saldo = cuenta.getSaldo();
            Object[] datos = {idCuenta, idUsuario, saldo};
            //AÃ±adir a la tabla
            modelo.addRow(datos);
        }
        modelo.setColumnIdentifiers(new String[]{"ID_Cuenta", "ID_Usuario", "Saldo"});
        this.resultados.setModel(modelo);
        modelo.fireTableDataChanged();

    }

