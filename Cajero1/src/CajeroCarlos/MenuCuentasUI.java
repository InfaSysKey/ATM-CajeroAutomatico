    /**
     * Borra la cuenta seleccionada
     */
    public void borrarCuentasSeleccionada() {
        int indice = this.resultados.getSelectedRow();

        int idCuenta = (int) this.resultados.getValueAt(indice, 0);
        ServicioBBDD.borrarCuenta(idCuenta);
        this.muestraCuentas();
    }

