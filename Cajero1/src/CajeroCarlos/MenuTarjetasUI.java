    /**
     *
     * @param evt Para modificar la linea seleccionada.
     */
    private void butModifiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_butModifiActionPerformed
        try {
            int fila = resultados.getSelectedRow();
            if (resultados.isRowSelected(fila)) {
                int idTarjeta = (int) resultados.getValueAt(fila, 0);
                int idCuentas = (int) resultados.getValueAt(fila, 1);
                int pin = Integer.parseInt((String) resultados.getValueAt(fila, 2));
                int bloqueo = (int) resultados.getValueAt(fila, 3);
                System.out.println(idTarjeta);
                System.out.println(idCuentas);
                System.out.println(pin);
                System.out.println(bloqueo);
                ServicioBBDD.updateTarjeta(idTarjeta, idCuentas, pin, bloqueo);
            }
        } catch (ClassCastException e) {
            JOptionPane.showMessageDialog(this, "Ningun dato actualizado", "Advetencia", JOptionPane.WARNING_MESSAGE);
        } catch (ArrayIndexOutOfBoundsException e) {
            JOptionPane.showMessageDialog(this, "Ninguna fila seleccionada", "Advetencia", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_butModifiActionPerformed

