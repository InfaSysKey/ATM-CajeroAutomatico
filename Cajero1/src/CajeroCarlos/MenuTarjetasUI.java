    /**
     * Muestra las tarjetas que hay. Con el boolean bloqueamos las celdas que
     * queremos.
     */
    public void muestraTarjetas() {
        List<Tarjeta> listaUsuarios = ServicioBBDD.getTarjetas();
        DefaultTableModel modelo = new DefaultTableModel(0, 4) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return column == 2;
            }
        };
        for (Tarjeta tarjeta : listaUsuarios) {
            int idTarjeta = tarjeta.getIdTarjeta();
            int idCuenta = tarjeta.getIdCuenta();
            int pin = tarjeta.getPin();
            int bloqueo = tarjeta.getTarjeta_bloqueo();

            Object[] datos = {idTarjeta, idCuenta, pin, bloqueo};
            //AÃ±adir a la tabla
            modelo.addRow(datos);
        }
        modelo.setColumnIdentifiers(new String[]{"ID_Tarjeta", "ID_Cuenta", "Pin", "Bloqueo"});
        this.resultados.setModel(modelo);
        modelo.fireTableDataChanged();

    }

